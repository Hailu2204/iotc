					<h3>Real Assets</h3>
                        <p align="justified">Our real assets platform capitalizes on IOTC Assets Fund global footprint, multi-disciplinary capabilities, extensive network of industry experts, and key relationships with operating partners. We adhere to IOTC investment philosophy, emphasizing the purchase of assets – or liens on assets – where we believe the relationship between risk and return is asymmetrical and where we believe relationships and a knowledge advantage can make a significant positive impact on our ability to successfully source, purchase, manage and exit investments.</p>
						<br/><br/>
						<h3>Real Estate</h3><br/>
						<p align="justified">Our real estate strategies target a diverse range of investments across nearly all areas of real estate, including core-plus, value-add and opportunistic real estate. We pursue equity and debt investments in a variety of property types in the Middle East, Europe, and Asia. Our extensive, global investment team is one of the largest and leverages joint-venture partnerships with high-quality real estate operators in an effort to deliver long-term outperformance across investment cycles.</p>
                    <br/><br/>
					<h3>Real Estate Opportunities</h3><br/>
						<p align="justified">IOTC Assets Fund pursues a wide range of global investment opportunities, across six areas of investment focus (commercial real estate, corporate real estate, structured finance, commercial non-performing loans, and residential real estate). Investments may include direct property investments; investments in real estate-related corporations; commercial mortgage-backed securities and related securities; residential land, assets and loan pools; small-balance commercial loan pools and investments. The group will also occasionally pursue development opportunities with aligned, high-quality partners. Our opportunistic approach and our team’s extensive expertise at the intersection of real estate and distressed debt allows us to capitalize on continued flow of over-leveraged and distressed real estate in need of rescue capital or asset repositioning.</p>
						<p align="justified">"Our investments emphasize bedrock philosophy of risk control," says John Brady, head of global real estate, "and fall into one or more of the following strategic areas, in order of priority: distress, inefficiency, value-added and long-term growth."</p>
						<p align="justified">The Real Estate group is particularly well positioned to leverage the firm's product, industry and geographical reach in global markets, where corporate and real estate assets are often bundled inefficiently. With dedicated multi-disciplinary strengths and global footprint.</p>
                    <br/><br/>
					<h3>Real Estate Debt</h3><br/>
						<p align="justified">IOTC Assets Fund launched the Real Estate Debt strategy as an expansion of the Real Estate Opportunities strategy. The first portfolio under this strategy umbrella was formed to purchase so called “RIF securities” – residential and commercial mortgage backed securities priced at significant discounts to par, which reflected the excessive use of leverage leading up the 2007-2008 global financial crisis. The Real Estate Debt strategy seeks to achieve attractive risk-adjusted returns and produce current income by investing in real estate-related debt that is not anticipated to result in control of the underlying asset.</p>
						<p align="justified">The strategy specializes in debt-driven opportunities across the Real Estate group’s six areas of investment focus (commercial real estate, real estate-related corporate investments, structure finance, commercial NPLs, residential real estate and real estate), and pursues a wide range of investments, including commercial and residential mortgages, mezzanine loans, and corporate debt.</p>
						<p align="justified">"We have a competitive advantage in our ability to identify an enhanced opportunity set by looking across a number of investment areas and by drawing upon IOTC Investment ’s expertise in “this strategy enables us to leverage our team and platform to capture a wider range of those debt-oriented opportunities we find to be attractive."</p>
						<p align="justified">Our approach adheres to IOTC Investment’s investment philosophy of risk control, consistency and bottom-up analysis. The Real Estate team works in close collaboration with other investment teams and an established network of high-quality partners throughout the research and investment management process.</p>
                    <br/><br/>
					<h3>Real Estate Income</h3><br/>
						<p align="justified">IOTC Investments launched the Real Estate Income strategy as a step-out of the Real Estate Opportunities strategy. It expands the reach of IOTC Assets fund platform through investments that have the potential to provide stable income and attractive risk-adjusted returns, but do not have the requisite distress or total return profile to be a candidate for Opportunities funds. The Real Estate Income strategy seeks to achieve superior risk-adjusted returns through investments in high-quality real estate assets with an emphasis on income and long-term growth.</p>
						<p align="justified">This strategy targets commercial real estate assets, with a emphasis on office, multifamily, industrial, and retail properties. It also considers debt and other income-producing investments on a limited basis.</p>
						<p align="justified">“We’ve grown IOTC Assets fund business organically, developing successful step-outs of our opportunistic real estate business “The Real Estate Income strategy positions us to capitalize on previously untapped investments with the goal of providing our clients with attractive current income and value-add upside potential.”</p>
						<p align="justified">Consistent with IOTC investments philosophy, the Real Estate Income strategy emphasizes the importance of market inefficiency, consistency and controlling risk. The team utilizes its global platform’s existing infrastructure and relationships to acquire core-plus and value-add real estate investments. The strategy mitigates downside risk by identifying price discounts relative to intrinsic value and replacement cost, diversifying positions by property type and geography, and conservatively managing leverage.</p>
                    <br/><br/>
					<h3>Infrastructure</h3><br/>
						<p align="justified">Our infrastructure strategies seek to make investments globally real assets and infrastructure-related businesses in the energy and transportation sectors, where we can achieve meaningful capital appreciation. By focusing on assets and businesses with both downside protection and substantial upside potential, our team seeks infrastructure investments in which we can pursue value-creation strategies geared toward sustainable growth.</p>
						<br/><br/>
					<h3>Infrastructure Investing</h3><br/>
					<p align="justified">Aging infrastructure assets require substantial investment, but capital constraints on the part of government entities are increasing the attractiveness of private participation in infrastructure development projects. At the same time, newly accessible supplies of energy are necessitating the expansion of existing infrastructure to connect production sources with end markets. IOTC Investments Infrastructure Investing strategy seeks to capitalize on these evolving industry dynamics by originating, owning and operating infrastructure and related investments, primarily in Globally. We do not seek investments in low-growth, annuity-like assets, but rather we pursue controlling or influential minority investments in strategic infrastructure assets exhibiting sustainable downside protection where our experience and knowledge advantage and our ability to add value to the assets provide significant upside potential.</p>
					<p align="justified">"We target investments where we see the potential to add significant value, principally through our operational, managerial, industry, risk management and financial expertise, With over a decade of experience investing in infrastructure, we have developed a deep understanding of the risk parameters and profile of successful infrastructure investments."</p>
					<p align="justified">IOTC Investments Infrastructure Investing strategy was added to the IOTC Investments platform as an expansion of our Power Opportunities strategy, which seeks to identify successful companies that provide the products and services that support infrastructure assets. Complementary sector knowledge and a broad network of industry relationships help us to identify attractive investment opportunities for both strategies. We believe important competitive advantages are derived from the collaboration between IOTC Investments Infrastructure Investing and Power Opportunities strategies as part of an integrated global platform, which provides a broad yet specialized array of corporate private equity and other strategies.</p>
                    
					
