					<h4><strong>Ports, Tunnels and Special Bridges Ports</strong></h4>
					
					<p align="justified">With in-depth knowledge of marine technology, maritime structures such as greenfield ports, breakwaters, docks, container terminals, bulk berths, liquid jetties, shipyards, ship lift systems and shore protection have been engineered and built to spearhead waterway transportation.</p>

					<p align="justified">In-house expertise in design, quarrying and state-of-the-art equipment ensure world-class quality and timely delivery.</p>
<br/>
					<h4><strong>Tunnelling</strong></h4>

					<p align="justified">With its rich experience of tunnelling in challenging altitudes , one of the world's toughest fold mountain ranges, IOTC Group has built expertise in all types, sizes and shapes of tunnelling with vast experience in construction of tunnels using various technologies such as the conventional Drill and Blast Method (DBM), the New Austrian Tunneling Method (NATM), Tunnel Boring Machines (TBM), Mechanical excavation or soft tunneling using road headers, etc.</p>

					<p align="justified">IOTC Group Construction is involved in tunneling for hydel projects and has deployed the tunnel boring machine for a tunnel length of about hydropower project. Soft tunneling employing road headers is being carried out.</p>
<br/>
					<h4><strong>SpecialBridges</strong></h4>

					<p align="justified">IOTC Group has bridged people and places in several countries across Asia with the finest bridges. Having pioneered the design and construction of major bridges, IOTC Group has extensive experience in executing a wide range of bridges of different span lengths using ingenious cutting-edge construction techniques such as:</p>
					<ul>
						<li>Incremental launching</li>
						<li>Segmental construction</li>
						<li>Cable-stay</li>
						<li>Precast, prestressed concrete</li>
						<li>Steel and concrete composite construction</li>
					</ul>
