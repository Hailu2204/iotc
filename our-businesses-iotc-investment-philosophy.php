					<h3>IOTC Investments Philosophy</h3><br/>
                    <p align="justified">The following reflects the investment philosophy and beliefs of IOTC Investments and its senior executives. IOTC Investment provides investment management where we believe the potential for reward outweighs the risk entailed. All our investment activities operate according to the unifying philosophy that follows:</p>
                    <br/><br/>
					<h3>The primacy of risk control</h3><br/>
                    <p align="justified">Our goal is not advisor or superior investment performance but superior performance with less-than-commensurate risk. Above-average gains in good times are not necessarily proof of a manager's skill.</p>
					<p align="justified">It takes superior performance in bad times to prove that those good-time gains were earned through skill, not simply the acceptance of above average risk. Thus, rather than merely searching for prospective profits, we place the highest priority on preventing losses. It is our overriding belief that, especially in the opportunistic markets in which we work, "if we avoid the losers, the winners will take care of themselves."</p>
                    <br/><br/>
					<h3>Emphasis on consistency</h3><br/>
                    <p align="justified">Oscillating between top-quartile results in good years and bottom-quartile results in bad years is not acceptable to us. It is our belief that a superior record is best built on a high batting average rather than a mix of brilliant successes and dismal failures.</p>
					<br/><br/>
					<h3>The importance of market inefficiency</h3><br/>
					<p align="justified">We feel that skill and hard work can lead to a "knowledge advantage," and thus to potentially superior investment results. But we do not believe this can occur in so-called efficient markets, where large numbers of participants share roughly equal access to information and act in an unbiased fashion to incorporate that information into asset prices. We believe there are less efficient markets in which dispassionate application of skill and effort should pay off for our clients, and it is only in such markets that we invest.</p>
                    <h3><blockquote style="text-align:center;">"If we avoid the losers, the winners will take care of themselves." </blockquote> </h3>
					
					<br><br>
					<h3>The benefits of specialization</h3><br/>
					<p align="justified">Specialization offers the surest path to the results we, and our clients, seek. Thus, we insist that each of our portfolios do just one thing — practice a single investment specialty — and do it absolutely as well as it can be done. We establish the charter for each investment specialty as explicitly as possible and do not deviate. In this way, there are no surprises; our actions and performance always follow directly from the job we're hired to do. The availability of specialized portfolios enables Oaktree clients interested in a single asset class to get exactly what they want; clients interested in more than one class can combine our portfolios for the mix they desire.</p>
                   <br/><br/>
				   
				   <h3>Macro-forecasting not critical to investing</h3>
					<p align="justified">We believe consistently excellent performance can only be achieved through superior knowledge of companies and their securities, not through attempts at predicting what is in store for the economy, interest rates or the securities markets. Therefore, our investment process is entirely bottom-up, based on proprietary, company-specific research. We use overall portfolio structuring as a defensive tool to help us avoid dangerous concentration, rather than as an aggressive weapon expected to enable us to hold more of the things that do best.</p>
                   <br/><br/>
				   
				   <h3>Disavowal of market timing</h3>
					<p align="justified">Because we do not believe in the predictive ability required to correctly time markets, we keep portfolios fully invested whenever attractively priced assets can be bought. Concern about the market climate may cause us to tilt toward more defensive investments, increase selectivity or act more deliberately, but we never move to raise cash. Clients hire us to invest in specific market niches, and we must never fail to do our job. Holding investments that decline in price is unpleasant but missing out on returns because we failed to buy what we were hired to buy is inexcusable.</p>
                   
