                                <h3>Scope of the HSE Management Systemy</h3>

                                <p align="justified">IOTC Group believes that its success depends on its approach,
                                    which emphasizes the necessity of continual improvement of the quality, environment,
                                    occupational health and safety systems.</p> <br/>
                                <p align="justified"><strong>The policy of the company consists of the following elements: </strong></p>
                                <p align="justified">The description of the “job” in IOTC Group is the performance
                                    of jobs in a way which is efficient, high quality, sensitive to nature, safe and
                                    healthy. Therefore, employees and subcontractors are encouraged to adopt the goal of
                                    "zero accidents" as a way of life.</p>
                                <p align="justified">It is committed to complying with the employers’ requirements, with applicable local
                                    and international legal requirements and with other HSE requirements. A safe working environment shall be provided for our employees, subcontractors,
                                    visitors and it is aimed to eliminate the hazardous and reduce the risks and
                                    evaluate the opportunities. It is expected that they will maintain standards exert
                                    the utmost efforts to improve upon them.</p>
                                <p align="justified">An effective internal communication with employees and employee representatives is
                                    provided to get participation in the management system. HSE trainings and awareness of employees and subcontractors shall be provided to
                                    increase their competency and promote their participation.</p>
                                <p align="justified">An effective communication line shall be established with all related parties. To ensure the protection of the environment, health, safety and security of all
                                    employees, all required resources shall be provided. Occupational health and safety risks and environmental impacts for all processes and
                                    activities shall be identified and managed by taking required measures to prevent
                                    pollution, injury and ill health. Minimizing the use of natural resources and waste during the entire design,
                                    procurement, construction, and commissioning phases of projects is a primary
                                    goal.</p>
                                <p align="justified">An environmentally friendly work methodology shall be adopted by keeping in mind that
                                    all activities have an environmental impact. The execution of the occupational health, safety and environmental management system
                                    shall be monitored and measured to improve the effectiveness of HSE management
                                    systems.</p>

                                <h3>Occupational Health, Safety and Environmental Policy.</h3>

                                <p align="justified">IOTC Group has been included in the scope of ISO 45001:2018,
                                    ISO 14001:2015 & ISO 9001:2015 HSE Management Systems with all activities.</p>
                                <p align="justified">The scope of IOTC Group operations in HSE Management Systems
                                    is, engineering, procurement and installation of all systems and facilities with the
                                    design, engineering and construction of airports, terminals, infrastructure works,
                                    construction structures, high rise buildings and high-tech structures buildings.</p>
                                <p align="justified">The scope of HSE Management System is applied to all regions/countries, geographic
                                    locations and IOTC Group headquarters and
                                    enterprises.</span></p>
