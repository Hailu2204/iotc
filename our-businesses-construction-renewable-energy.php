					<p align="justified">IOTC Group is committed to playing a significant role in the arena of renewable energy by spearheading the green revolution in India. Over the years, IOTC Group has gained expertise in different solar technologies to design and execute solar power plants including financing assistance for solar projects of different capacities.</p>

					<p align="justified"><strong>Pre-bid Services: </strong>IOTC Group provides bid management services and load-flow study coordination to key clients and developers interested in bidding for projects under various national and State policy tenders in a pre-bid alliance mode.</p>

