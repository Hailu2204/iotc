					<p align="justified"><strong>Procurement: </strong>Access to the best technology solutions is facilitated by IOTC's wide experience across a global supply chain and long-term component partnerships it shares with supply partners. IOTC Group techniques optimize resources and provide ISO and OHSAS-certified facilities for manufacturing structures.</p>

					<p align="justified"><strong>Construction: </strong>World-class construction management and planning with single point responsibility give IOTC Group an edge over competition. Timely project completion is ensured within the estimated budget, thus establishing a long-term sustainable solution for the customer in the shortest time.</p>

					<p align="justified"><strong>Operations & Maintenance: </strong>Post commission, monitoring, operations and maintenance support with tailor-made O&M packages for seamless operation (Turnkey +) is also on offer. Apart from established processes and procedures for proactive and reactive (breakdown) maintenance, IOTC Group offers 24x7 site surveillance support for very large solar farms. Smooth functioning is ensured using a remote monitoring system at IOTC Technical HQ in Chennai, real-time plant technician support and a well-networked communication channel with transmission / grid operator and plant owners.</p>
