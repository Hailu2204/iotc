                <div class="hydroUpstreamIntro"><br/>
                    <p align="justified">IOTC Group has built roads and motorways in and around the world that have involved major earth-moving operations. The company has also built large-section road tunnels, including the associated safety, lighting, ventilation &amp; fire-prevention systems. The works performed range from roads in the African interior to the modern motorways of industrialised countries, including virtually all types of surfacing in both rigid concrete Roads & bridges &amp; flexible asphalt.</p>
					<br/>
					<h4 align="justified"><strong>The Group has built bridges and viaducts using all kinds of techniques: </strong></h4>
					
					<p align="justified">from the simplest constructions, with girder decks and pre-cast or cast-in-place concrete caissons, to more complex structures such as cable-stayed and suspended bridges. The Group also has extensive experience in the construction of bridges and viaducts with self-launched concrete decks or with metal decks and in the construction of piers of every type, directly on rock or on large-diameter piles, micro piles and pile cylinders.</p>

					<p align="justified">Whether it is the acoustics of the desolate desert or the deafening clamour of city traffic, for IOTC Group Construction's Transportation Infrastructure business, complexity has always sounded as opportunity. Powering the infrastructure segments comprising building and upgrading of roads, runways, elevated corridors and railways, IOTC Group enlivens the lifelines of nations.</p>
<br/>
					<h4><strong>Road</strong></h4>
					<p align="justified">Creating high quality transportation infrastructure in globally is one of the prime operations for IOTC Group's Transportation Infrastructure vertical. Having designed and constructed the full spectrum of highways, small to large, urban to rural, simple to complex, IOTC Group has to its credit in build road and several bridges which include expressways, National / State highways, toll plazas and complex road interchanges in  globally. Expertise also covers the construction of race tracks / test tracks for high speed motor sport that demands precision and specialized surface treatment.</p>
<br/>
					<h4><strong>Runway</strong></h4>
					<p align="justified">IOTC Group provides end-to-end solutions for the design and construction of airside infrastructure of both greenfield and brownfield airports for civil and defence purposes through its broad spectrum of capabilities. Airside infrastructure includes runways incorporating CAT IIIB instrument landing systems, taxiways, aprons and service roads, drainages, fire andfuel lines, storm water drains, airfield ground lighting and approach lights, high fill embankments and landscaping incorporating green features.</p>

					<p align="justified">Most of the runways built by IOTC Group at international airports are compatible with the landing of new generation wide-bodied aircrafts like the Airbus A-380.</p>
<br/>
					<h4><strong>Elevated Corridors</strong></h4>
					<p align="justified">IOTC Group's pedigree in innovation, structural design and execution proves its position as a leader in elevated corridors which has translated into the fine delivery of challenging projects. The business segment has expertise in the design and construction of a wide array of elevated civil structures such as elevated corridors, grade separated concrete and steel structures.</p>

					<p align="justified">Capability also includes design and construction of super structures using segmental construction, launching girders, prestress precast I / box girders, steel composite girders and balanced cantilever structures.</p>
					<br/>
					<h4><strong>Railway</strong></h4>
					<p align="justified">Railways are the lifeline of nations and IOTC Group has established itself as a unique service provider for delivering turnkey solutions for all types of projects in the rail and urban transit sector. IOTC Group offers Engineering, Procurement and Construction solutions encompassing design, engineering, supply, project planning, quality control and field execution.</p>

					<p align="justified">At the forefront of building railway infrastructure for mainline and dedicated freight corridors, IOTC Group has been meeting the demands of the country by applying project management competencies, new technologies and mechanized construction methods. With a proven track record and more than three decades of experience, IOTC Group has introduced several pioneering techniques for the execution of projects with innovation, quality and speed.</p>

					<p align="justified">IOTC Group is the only private organization which is qualified to undertake integrated rail construction projects for the Indian Railways. Its expertise includes Ballast less Tracks, Rigid Overhead Conductor Systems (ROCS) and Gas Insulated Switchgear (GIS). IOTC railway construction business segment has played a lead role in constructing India's first monorail system at Mumbai and also brings in extensive experience of working on large-scale underground rail infrastructure.</p>
					<br/>
					<h4 align="justified"><strong>Capability Spectrum:</strong></h4>
					<ul>
						<li>Integrated solutions for railway construction</li>
						<li>Railway electrification</li>
						<li>Dedicated rail links and merry-go-round systems</li>
						<li>Complete civil and track works</li>
						<li>Signaling and telecommunication</li>
						<li>Steel / concrete bridges and tunnels</li>
					</ul>
