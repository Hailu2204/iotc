						<p align="justified">IOTC has played a leading role in technical safety research for many years. Our research has supported the development of safe designs for on- and offshore LNG facilities, terminals and ships. Multiple layers of protection at LNG facilities are designed to reduce the risk of incidents: for example, by minimising any confined spaces where LNG vapour could accumulate and removing ignition sources.
						<p align="justified">IOTC joint ventures operate a diverse portfolio of LNG plants and terminals around the world.</p>
						<p align="justified">Process safety involves making sure our facilities are well designed, safely operated and properly maintained to prevent leaks of hazardous materials. We use structured processes to manage our asset integrity and prevent leaks, spills and any other technical failures or breakdowns. Process safety starts at the early design phase of building facilities and continues throughout their life cycle, making sure they are operated safely, well maintained and inspected regularly to identify and deal with any potential process safety hazards.</p>
						<br/><br/><h3>Clear global standards</h3><br/>
						<p align="justified">Our global technical safety standards meet local regulatory requirements and, in many cases, exceed them. They define how we manage our facilities during their complete life cycle, with the goal of preventing incidents such as leaks and spills.</p>
						<p align="justified">We take a dual approach to potential incidents. We identify and assess risks that have the potential to become an incident, and take the necessary steps to mitigate or eliminate them. At the same time, we prepare for, and are ready to respond to, incidents that occur. We learn from investigations into industry incidents and embed this knowledge into our technical safety standards. Our training programmes help to create a working culture that increases risk awareness and prevents safety incidents.</p>
						<br/><br/><h3>Emergency response</h3><br/>
						<p align="justified">We develop plans, and make sure we have the necessary resources to deal with spills, leaks, fires or explosions. We review our emergency response plans, testing them in safety exercises or in drills with local services and regulatory agencies that would be involved if an incident took place. We also create drill scenarios which involve the safe evacuation of our workforce from platforms, refineries or office buildings. This helps to test and improve our plans.</p>
						<p align="justified">For oil spills, we have created a global response network that enables us to deal more effectively with oil spills, supplementing local response capability. We routinely perform large-scale exercises with local regulatory and response organisations to practice, and improve, our response capability.</p>
